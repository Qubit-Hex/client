/**
 * 
 *  @component: FuzzyMark
 * 
 *  @descriptions: This is a visual component that will be used to display the data.
 */

const FuzzyMark = (props) => {

   // high intensity of the data 
    const generateRedShades = () => {
      
        return 'rgba(255, 0, 0,' + Math.random() * (0.60, 1.00) + 0 + ')';
    }

    // normal intensity of the data
    const generateGreenShades = () => {
        return 'rgba(0, 128, 0,' + Math.random() * (0.60, 1.00) + 0 + ')';
    }

    // medium intensity of the data
    // orange 
    const generateOrangeShades = () => {
        return 'rgba(255, 165, 0,' + Math.random() * (0.60, 1.00) + 0 + ')';
    }

    // low intensity of the data
    const generateYellowShades = () => {
        return 'rgba(255, 255, 0,' + Math.random() * (0.60, 1.00) + 0 + ')';
    }

    // blue intensity of the data
    const generateBlueShades = () => {
        return 'rgba(0, 0, 255,' + Math.random() * (0.60, 1.0) + 0 + ')';
    }


    const weight = props.data.weight;

    if (weight >= 0 && weight <= 2.5) {
        return ( <div className='fuzzyMarker red-shadow' style={{ backgroundColor: generateRedShades() }}> </div> );
    } else if (weight >= 2.5 && weight <= 5) {
        return ( <div className='fuzzyMarker orange-shadow' style={{ backgroundColor: generateOrangeShades() }}> </div> );
    } else if (weight >= 5 && weight <= 7.5) {
        return ( <div className='fuzzyMarker yellow-shadow' style={{ backgroundColor: generateYellowShades() }}> </div> );
    } else if (weight >= 7.5 && weight <= 10) {
        return ( <div className='fuzzyMarker green-shadow' style={{ backgroundColor: generateGreenShades() }}> </div> );
    } else {
        return ( <div className='fuzzyMarker blue-shadow' style={{ backgroundColor: generateBlueShades() }}> </div> );
    }

}


/**
 * 
 *  @function: interpolateDistance
 *  
 * 
 *  @description: This function is used to interpolate the distance between two points. base on the output it will be 
 *                place into a bucket {0, 1, 2, 3}
 *                0 - low
 *                1 - medium
 *                2 - high
 *                3 - very high 
 */


const GeneratePoints = (props) => {


    // calculate the distance between the two points
    // in order to get generate a weighted distribution of the data
    // this will generated to generate the weight 
    const calculateDistance = (x1, x2, y1, y2) => {
        const x = x1 - x2;
        const y = y1 - y2;
        const distance = Math.sqrt(x * x + y * y);
        return distance;
    }

    // wait for the data to populate otherwise this will trigger an error
    if (props.data.length > 1) {

        // sort props.data by longitude and latitude
        // we this to get closest point other wise 
        // we have to use a G = (E, V) algorithm in form
        // a Archimedean spiral go through the points
        // in order to find the relationships between the points. 

        const sortedData = props.data.sort((a, b) => {
            if (a.lon < b.lon) {
                return -1;
            }
            if (a.lon > b.lon) {
                return 1;
            }
            return 0;
        });


       

        let bucket = [];

        // sort the data of our distribution of data nodes 
        for (let i = 0; i < sortedData.length; i++) {
            if (i+1 < sortedData.length) {
            
                    // calculate the distance between the two points
                    const x1 = sortedData[i].lon;
                    const x2 = sortedData[i + 1].lon;
                    const y1 = sortedData[i].lat;
                    const y2 = sortedData[i + 1].lat;

                        bucket.push({
                            name: sortedData[i].name,
                            lon: sortedData[i].lon,
                            lat: sortedData[i].lat,
                            weight: Math.round(calculateDistance(x1, x2, y1, y2))
                        });
                
            } else {
               // no distance to calculate so return a generic distance weight
                bucket.push({
                    name: sortedData[i].name,
                    lon: sortedData[i].lon,
                    lat: sortedData[i].lat,
                    weight: 10
                });
            } 
        }
                // return the bucket for processing
            return (
                <Map height={1200}  defaultCenter={[43.6426, 43.6426]} defaultZoom={3}>  
                <ZoomControl />
                {
                    bucket.map((item, index) => {
                        return (
                            <Overlay key={index} anchor={[item.lat, item.lon]}>
                                <FuzzyMark data={item} onClick={
                                    (e) => {
                                        console.log(item);
                                    }
                                } />
                            </Overlay>
                        )
                    })
                }
               
              </Map>
          
            );  
        }  else {
            return (
                <div>
                    <p>Loading...</p>
                </div>
            );
        }

}


